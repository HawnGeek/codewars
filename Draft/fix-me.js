function fixMe() {
  var numbers = [];
  
  // take advantage of ES6 block scoping to avoid having `i`
  // always reference closure scope value of 10
  for (let i=0; i < 10; i++) {
      numbers[i] = function () {
        return i;
      };
  }

  return numbers;
}


